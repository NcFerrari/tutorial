# názvy proměnných musí začínat jen klasickými znaky nebo podtržítkem (prostě klasika)
alfa = 1
print(alfa)

# konvence jsou stejné jako všude jinde. Stačí používat camelCase a jsem vykrytý

alfaBetaGama = "řecká písmena"
print(alfaBetaGama)

# případně se může použít tedy podtržítko

_variable = 21
print(_variable)

# mohou být různé syntaxe:

# camelCase
variableOne = "variableOne"
print(variableOne)
# PascalCase
VariableTwo = "VariableTwo"
print(VariableTwo)
# CAPITAL_CASE
VARIABLE_THREE = "VARIABLE_THREE"
print(VARIABLE_THREE)
# snake_case
variable_four = "variable_four"
print(variable_four)
# nebo s podtržítkem
_variable_five = "_variable_five"
print(_variable_five)
