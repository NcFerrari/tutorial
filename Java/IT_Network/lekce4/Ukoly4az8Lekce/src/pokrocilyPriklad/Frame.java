    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if (model.getWinWord().contains(jTextField1.getText().toUpperCase())) {
            model.correctTip(jTextField1.getText().toUpperCase());
            winWordLabel.setText(model.getTips());
        } else {
            wrongTips++;
            checkEnd(wrongTips);
        }
        if (model.getWinWord().equals(model.getTips())) {
            WinDialog winDialog = new WinDialog(this, true);
            winDialog.setWrongTips(wrongTips);
            winDialog.setVisible(true);
            newGame();
        }
        jTextField1.setText("");
        jTextField1.requestFocus();
    }//GEN-LAST:event_jButton1ActionPerformed

