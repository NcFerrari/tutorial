    private void checkEnd(int wrongTips) {
        imageLabel.setIcon(new ImageIcon(getClass().getResource("/sources/img/" + wrongTips + ".png")));
        if (wrongTips == maxTips) {
            LoseDialog loseDialog = new LoseDialog(this, true);
            loseDialog.setWord(model.getWinWord());
            loseDialog.setVisible(true);
            newGame();
        }
    }
